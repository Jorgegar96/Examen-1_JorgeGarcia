    public boolean usuarioExistente(String username) {
        for (Familiar familiar : familiares) {
            if (familiar.getName().equals(username)) {
                return true;
            }
        }
        for (Personal personal1 : personal) {
            if (personal1.getName().equals(username)) {
                return true;
            }
        }
        if (Kim.getName().equals(username)) {
            return true;
        }
        return false;
    }

    public boolean contrase単aCorrespondiente(String username, String password) {
        for (Familiar familiar : familiares) {
            if (familiar.getName().equals(username) && familiar.getPassword().equals(password)) {
                logged = familiar;
                return true;
            }
        }
        for (Personal personal1 : personal) {
            if (personal1.getName().equals(username) && personal1.getPassword().equals(password)) {
                logged = personal1;
                return true;
            }
        }
        if (Kim.getName().equals(username) && Kim.getPassword().equals(password)) {
            logged = Kim;
            return true;
        }
        return false;
    }

